<?php
    //Integer 
    echo "Decimal : ";
    var_dump(12345);

    echo "Octal : ";
    var_dump(01234);

    echo "Hexadecimal : ";
    var_dump(0x1A);

    echo "Underscore : ";
    var_dump(1_000_000_000);

    //Float
    echo "Floating : ";
    var_dump(1.234);

